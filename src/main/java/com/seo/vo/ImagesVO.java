package com.seo.vo;

public class ImagesVO {
	
	int compositor;
	String image_url;
	String image_ftpurl;

	public int getCompositor() {
		return compositor;
	}
	public void setCompositor(int compositor) {
		this.compositor = compositor;
	}
	public String getImage_url() {
		return image_url;
	}
	public void setImage_url(String image_url) {
		this.image_url = image_url;
	}
	public String getImage_ftpurl() {
		return image_ftpurl;
	}
	public void setImage_ftpurl(String image_ftpurl) {
		this.image_ftpurl = image_ftpurl;
	}

	
	
}
